# Python has a set of built-in math functions, including an extensive math module, that allows you to perform mathematical tasks on numbers.

import math
x=min(1,5,28,29,22,24)
print(x)
x=max(1,5,28,29,22,24)
print(x)
x=math.ceil(21.56)
print(x)
x=math.floor(21.56)
print(x)
x=abs(-21.56)
print(x)
x=pow(4,3)
print(x)
x = math.sqrt(64) 
print(x)
x = math.cbrt(64)
print(x)
